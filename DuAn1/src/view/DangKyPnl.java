    public void dangKy() {
        String gt = "";
        if (rdbnam.isSelected() == true) {
            gt = "0";
        } else {
            gt = "1";
        }
        try {
            userDAO dao = new userDAO();

            dao.insert("insert into USERS (TaiKhoan,matKhau,Email,gioiTinh,vaitro) values (?,?,?,?,0)",
                    txttaikhoan.getText(), txttaikhoan.getText(), txttaikhoan.getText(), gt);
            JOptionPane.showMessageDialog(this,"Đăng kí thành công");
        } catch (Exception e) {
            System.out.println("Exception in Dang Ky");
        }
    }

