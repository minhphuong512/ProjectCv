    boolean checkGioiTinh() {
        if (rdbnam.isSelected() == true || rdbnu.isSelected() == true) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Phải chọn giới tính");
            return false;
        }
    }
    boolean checkTrung(JTextField txt){
        txt.setBackground(white);
        if (dao.findAll("select * from USERS where TAIKHOAN like N'?'", txttaikhoan.getText()) == null) {
            return true;
        } else {
            txt.setBackground(pink);
            JOptionPane.showMessageDialog(this, txt.getText() + " đã bị tồn tại.");
            return false;
        }
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (untility.utilityCheck.checkNullText(txttaikhoan)
                && untility.utilityCheck.checkNullText(txtpass)
                && untility.utilityCheck.checkNullText(txtpassrepeat)
                && untility.utilityCheck.checkNullText(txtemail)
                && untility.utilityCheck.checkEmail(txtemail)
                && untility.utilityCheck.checkNullText(txtten)
                && checkGioiTinh() && checkTrung(txttaikhoan)) {
            if(!txtpass.getText().equals(txtpassrepeat.getText())){
                JOptionPane.showMessageDialog(this,"Không trùng mật khẩu");
            }else{
                dangKy();
            } 
        }

    }//GEN-LAST:event_jButton2ActionPerformed

